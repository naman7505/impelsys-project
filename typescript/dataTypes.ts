//Data Types

//1. "number" Data Types under nummber int and float both can be written.
var a:number=10;          //Integer
var b:number=10.11;       //Float number
console.log("Integer number is : "+a);
console.log("Floating number is : "+b);

//2. "string" Data Types
var c:string="Ayush Weds Shikha";    //string value
console.log("String is : "+c);

//"boolean" Data Types values can be true/false
var i:boolean=true;
console.log("Boolean value is : "+i);

//4. "any" Data Types under any data types we can append anything like object, array,string,integer,boolean
var d:any={fname:"Ayush", lname:"Kesarwani"};
var e:any=["Ayush", "Shikha","Abhishek","Sanjeev","Prakash","Ankit"];
var f:any=1000;
var g:any="Hello I am Ayush";
var h:any=true;
console.log("Object of 'any' is : "+d);
console.log("Array of 'any' is : "+e);
console.log("Integer of 'any' is : "+f);
console.log("String of 'any' is : "+g);
console.log("Boolean of 'any' is : "+h);
